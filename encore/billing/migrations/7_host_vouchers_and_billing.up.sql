CREATE TABLE host_voucher_codes (
  id TEXT PRIMARY KEY,
  code TEXT NOT NULL UNIQUE,
  campaign TEXT NOT NULL,
  duration_months INTEGER NOT NULL,
  status TEXT NOT NULL DEFAULT 'available',
  redeemed_by TEXT,
  redeemed_at TIMESTAMPTZ,
  free_starts_at TIMESTAMPTZ,
  free_ends_at TIMESTAMPTZ,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX host_voucher_codes_status_idx ON host_voucher_codes (status);

CREATE TABLE host_billing_accounts (
  user_id TEXT PRIMARY KEY,
  plan TEXT NOT NULL,
  billing_source TEXT NOT NULL,
  billing_status TEXT NOT NULL,
  voucher_code TEXT,
  voucher_redeemed_at TIMESTAMPTZ,
  current_period_start TIMESTAMPTZ,
  current_period_end TIMESTAMPTZ,
  reminder_window_starts_at TIMESTAMPTZ,
  last_reminder_sent_at TIMESTAMPTZ,
  reminder_count INTEGER NOT NULL DEFAULT 0,
  card_on_file BOOLEAN NOT NULL DEFAULT FALSE,
  cardholder_name TEXT,
  card_brand TEXT,
  card_last4 TEXT,
  card_expiry_month INTEGER,
  card_expiry_year INTEGER,
  greylisted_at TIMESTAMPTZ,
  greylist_reason TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX host_billing_accounts_status_idx ON host_billing_accounts (billing_status);
CREATE INDEX host_billing_accounts_source_idx ON host_billing_accounts (billing_source);

CREATE TABLE host_billing_events (
  id TEXT PRIMARY KEY,
  user_id TEXT NOT NULL,
  event_type TEXT NOT NULL,
  actor_id TEXT,
  metadata JSONB,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX host_billing_events_user_id_idx ON host_billing_events (user_id, created_at DESC);

